        <h3>Implementation: Farmers and beekeepers</h3>

        <p>
          An essential part of implementation is to understand the end-users
          benefits and interests. Some of the stakeholders we considered were
          <b>farmers</b>, that could apply Let.it.bee. in their crops, and
          <b>beekeepers</b>, whose bees can suffer from the disappearance
          phenomenon caused by pesticides.
        </p>

        <p>
          The pandemic was a big obstacle to our communication with them. In the
          initial part of our project, we planned to eventually establish a
          conversation in person, when the pandemic was in a better situation,
          as it was being very hard to reach them through on-line methods. But
          <b>the situation never got better enough, unfortunately.</b>
        </p>

        <p>
          We resorted to trying to communicate through <b>Facebook groups</b>,
          where we found communities of farmers and beekeepers communicating in
          posts. We wrote a post quickly and simply explaining our projects and
          objectives, as well as the contextualization of our project, so it
          could be divulgated in those groups with permission and gather
          feedback.
        </p>

        <p>
          We focused on: the <b>importance of bees to agriculture</b>, the
          <b>misuse of pesticides</b>, the general
          <b>description of our project</b> and questions about
          <b>possible interests in the initiative.</b>
        </p>

        <h3>What was the result?</h3>

        <p>
          Although even in this situation the communication was hard, as it
          depended on the groups' divulgation of our post, we managed to gather
          a few interesting pieces of data that point to the fact that there is
          still a lot of
          <b
            >misinformation about bees' importance and the correct use of
            pesticides.</b
          >
          Many did not fully know about their relation to agriculture or the
          relation of pesticides to the bees' disappearance and were very
          concerned about the phenomenon.
        </p>

        <p>
          In the future, with the improvement of the pandemic condition in our
          country, it would be very crucial to try to meet with them to gather
          more feedback, as well as develop an
          <b
            >educational material explaining the problematic scenario, the
            correct use of pesticides and the benefits of bees' pollination</b
          >, which could help convince people to adhere to changes.
        </p>

