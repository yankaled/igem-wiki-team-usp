        <h3>What was the result?</h3>

        <p>
          In summary, we understood that the
          <b>real importance of bees for the world</b> is much more extensive
          than we originally thought, only then
          <b>grasping at the full scenario of the phenomenon.</b> The most
          important facts we learned were:
        </p>

        <ul>
          <li>
            <b
              >Bees have a major participation in the economy and food
              production</b
            >, by pollinating crops. Losing them would mean products with less
            quality and less quantity, harming farmers and companies in the
            process.
          </li>
          <li>
            Neonicotinoids have an especially aggressive effect on bees because
            <b
              >even a little bit of contaminated pollen can harm and destroy an
              entire colony.</b
            >
          </li>
          <li>
            Still, <b>they are largely used around</b> the world despite having
            been <b>highly associated with the bees' disappearance.</b>
          </li>
          <li>
            We learned about some
            <b>enzymes that could possibly detoxify neonicotinoids.</b> They are
            not naturally present in bees' genomes, so there was a possibility
            of their action being very helpful for detoxification.
          </li>
        </ul>

        <p>
          Through the contact with <b>A.B.E.L.H.A,</b> through Dr. Ana Lucia
          Assad, we also learned:
        </p>
        <ul>
          <li>
            What kind of projects are already being done when it comes to bee
            conservation, <b>how our project could help</b> along with these
            other initiatives
          </li>
          <li>
            That
            <b
              >there haven't been any other projects like ours, and that it
              could even be commercially interesting</b
            >
            to some agribusiness companies.
          </li>
          <li>
            She also directed us to her contact in <b>Embrapa</b>, a public
            agricultural research corporation and
            <b>one of the biggest names in sustainable development</b> here in
            Brazil, and also <b>Ouro Fino</b>, Brazilian
            <b>manufacturer of crop protection products.</b>
          </li>
        </ul>
        <p>
          This set our plan and objectives for the project:
          <b
            >to protect bees from the neonicotinoids by degrading them with an
            enzyme, with the goal to have a positive impact on the economy and
            environment.</b
          >
          The next step was to fully design it. <span id="Idea"></span>
        </p>

        <h2>Idealizing the initial design</h2>

        <p>
          With the discoveries, we began to formulate our project and understand
          what agents could be targeted. We decided to target the neonicotinoid
          directly at its source and try and express
          <b>our desired modification on a plant</b> thus protecting the
          pollinators that would come into contact and their colony aswell.
        </p>

        <p>
          Among all types of neonicotinoids, we settled on working with
          <b>imidacloprid</b>, since it is the most used pesticide around the
          world, so our project could be more comprehensive and potentially
          apply more crops [3].
        </p>

        <p>
          By researching in literature, we found reports of many enzymes from
          the CYP P450 superfamily that were able to metabolize neonicotinoids
          and other types of pesticides, options that we could use for our
          project. Then, we contacted a
          <b>specialist in plant molecular biology</b> in our university,
          <b>Dr. Igor Cesarino</b>, who became one of our Principal
          Investigators (PIs). With his help, we were able to fully finish the
          <b>first draft of our project's design</b>:
        </p>

        <div class="has-text-centered">
          <br />
          <p>
            <b>Generally expressing a metabolizing enzyme in a plant</b>, to
            make it safe for bees.
          </p>
          <br />
        </div>

        <p>
          His counseling also led us to our initial model plant, the
          <b>Arabidopsis Thaliana</b>, because it has a small size, a small life
          cycle and it is also a well-known scientific model, features that
          would be very helpful for our project's planning and development.
          <span id="Design"></span>
        </p>

