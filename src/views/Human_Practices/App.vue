        </p>

        <h2>After iGEM</h2>
        <h3>Faculty Meeting</h3>

        <p>
          We wanted to create an event that could gather many
          <b
            >members from the faculty of several universities specialized in
            various parts of our project</b
          >: from bee behavior and ecotoxicology to molecular biology, synthetic
          biology, mathematical modeling, chemical substances, plant physiology,
          among other topics.
        </p>

        <p>
          The objective was to show them our projects and
          <b
            >gather feedback from multiple areas** to improve them, as well as
            introduce them to our team.</b
          >
        </p>

        <p>
          It was a <b>two-part webinar,</b> between which the professors were
          divided into according to their specialization (but they could see as
          many events as they wished):
        </p>

        <ul>
          <li>
            One focusing on the <b>bees' disappearance situation</b> (what are
            the agents, what is happening to them, what do we want to change)
            and the <b>molecular/synthetic biology</b> aspects, such as our
            circuit, the experiments we planned to do and our chosen chassis.
          </li>
          <li>
            And the other focusing on the
            <b><i>in silico</i>and mathematical modeling</b> section of our
            project, what were the modeling projects, what questions were we
            trying to answer and how.
