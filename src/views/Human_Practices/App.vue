        <p>
          The regulation for genetically modified organisms can be a bit tricky
          and safety is a very necessary discussion for the development and
          possible implementation of Let.it.bee.. Luckily for us, we found
          <b>The Cornell Alliance for Science</b>, an organization from Cornell
          University that seeks to promote access to scientific innovation as a
          means of enhancing food security, improving environmental
          sustainability, and raising the quality of life globally.
        </p>

