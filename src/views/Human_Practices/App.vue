        <h3>What was the result?</h3>

        <p>
          From this step in our Human Practices, we gathered even more material
          about the scenario and the
          <b>use of the pesticide we intend to work on</b>. The data were
          crucial for our project's design, aiding us in improving it. The most
          important information we learned were:
        </p>

        <ul>
          <li>
            The most used method of application for imidacloprid is
            <b>seed treatment</b>.
          </li>
          <li>
            Imidacloprid is very effective as a pesticide and, because of that,
            it also makes it an <b>environmentally safer option</b> for crops.
          </li>
          <li>
            Colony Collapse Disorder is officially reported in USA and Europe,
            but not in Brazil. But here the main cause for the bees'
            disappearance is <b>acute intoxication</b>, strongly related to
            pesticide use.
          </li>
        </ul>

        <p>
          Putting these facts together, we realized that
          <b>maintaining the function of imidacloprid</b> was very important,
          therefore we could not degrade it in the whole plant's body. We needed
          to focus on tissues that put bees, and other pollinators, at risk:
          <b>the pollen</b>. As Dr. Roberta Nocelli, in our first segment, had
          explained, it is not usually used by other insects, thus it was the
          perfect target for our project. We had to come up with a
          <b
            >method for our enzyme to be specifically expressed in that region,
            through the use of a locally expressed promoter with minimal off
            targets.</b
          >
        </p>

        <p>
          Through our PI, Dr. Igor Cesarino, we came into contact with
          <b>Dr. Maria Magdalena Rossi</b>, who is also specialized in plant
          molecular biology, with a focus on tomato plants. With their guidance,
          we decided to <b>change our model plant to the Micro-Tom</b>(<i
            >Solanum lycopersicum</i
          >), aiming for the best possible result for our project, for a long
          range of reasons:
        </p>
        <ul>
          <li>
            <b><i>A. thaliana</i> does not have an optimal growth</b> in our
            university (as opposed to Micro-Tom). It could endanger the small
            time we had for it to grow enough for experiments, as well as it
            could possibly not have enough material for us to collect, since we
            would need to test pollen.
          </li>
          <li>
            <b><i>A. thaliana</i> is not a crop</b>, it does not have a
            commercial interest as much as it has a huge importance for
            research.
          </li>
          <li>
            They share <b>important features for model plants</b>: short life
            cycle, small size, high density, easy to grow and transform.
          </li>
        </ul>

        <p>
          So we settled on a final decision of using the Micro-Tom as our
          chassis, also locking our promoter decision based on its genome. That
          was essential for the completion of our circuit's design.
        </p>

        <p>
          Another important thing about the meetings is that they brought up a
          very important question for our future implementation, which is the
          spray application of imidacloprid that could perhaps affect bees that
          come in contact with contaminated pollen surfaces. The misuse of
          pesticides seems to be a huge problem, considering that protocols are
          not always being applied, which could prevent deaths caused by
          possible spray applications. Although that can be of worry, the main
          method for the substance is still seed treatment, guaranteeing the
          imidacloprid in pollen to be metabolized [4]. Thus, it is possible for
          it to be absorbed into the plant's system when not lost through
          environmental factors like wind and sunlight. Either way, it is
          possible to improve Let.it.bee. in the future, with new mechanisms.
        </p>

        <p>
          We can summarize the important results as being the final design of
          our project:
          <b
            >a circuit with a pollen-specific promoter, a CYP P450 enzyme (with
            a system for optimized reporter gene expression), to be expressed as
            a test in Micro-Tom.</b
          >
