        <h3>What was the result?</h3>

        <p>
          The conversation was very important for us to settle our next steps
          and guarantee the safety aspect of Let.it.bee. The main points were:
        </p>

        <ul>
          <li>
            There are still many
            <b>negative stereotypes surrounding synthetic technology</b>,
            especially when it comes to food. However, this scenario is
            changing, little by little, and we hope that our project's
            engagement, human practices and divulgation can help that, by
            showing that GMO can be used for multiple good causes.
          </li>
          <li>
            We needed the opinions of <b>end users and companies</b> for a more
            complete feedback.
          </li>
          <li>
            We needed to
            <b>deeply research Brazil's regulation on GMO approval</b>, so
            Let.it.bee. can be safely commercialized in the future as well as
            safely developed.
          </li>
        </ul>

        <p>
          Following that conclusion, we invested in the
          <b>scientific divulgation</b> of our project, by taking information
          related to our project and GMO history and processes involved in their
          approval or making. The public education and engagement initiatives
          also tried to bring more information about synbio to the public.
        </p>

        <p>
          Moreover, we went after the
          <b>Brazilian regulation over GMO and patents</b>, searching the main
          organizations related to it, such as CTNbio (the institute responsible
          for GMO biosafety and policies) and MAPA, our Ministry of Agriculture,
          Livestock and Supply, responsible for guidance in approval of new
          cultivars. From them, we managed to develop our project as safely as
          possible along with the guidelines and also learned about what tests
          and procedures our plant would have to go through in the future,
          proving its function, safety and stability.
