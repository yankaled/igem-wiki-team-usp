        <p>
          After finding the optimal procedures for RNA extraction, removal of
          DNA residues and the knowledge of minimum RNA quantity necessary to
          synthesize cDNA from pollen, we started the genomic DNA extraction
          from all transformant plants to find positive transformants for the
          presence of the circuit LAT52-CYP6G1-GUS. As we didn't have extraction
          kits to use on all plants, we extracted the DNA with the protocol
          described by Vennapusa (2021) [3] adapted by students from Laboratory
          of Bioluminescent Fungi - Institute of Chemistry - USP, Brazil. The
          adaptation consisted of breaking the plant cell walls with 1 mL of RNA
          Extraction Buffer at room temperature with support of the SpeedMill
          PLUS Homogenization System (Analytik Jena). Five cycles of 1 minute of
          agitation and 1 minute of rest were made at room temperature. We
          changed the acid phenol saturated solution (ph 4.3) to basic phenol
          solution (pH >7.4) to isolate DNA only. We added the steps for
          precipitation of total RNA using sodium acetate solution 3 M pH 5.2 at
          0.1x of lysate total volume, following the addition of 3x of total
          volume with pure ethanol. The solution was inverted 20 times, and kept
          on ice for 30 min. The RNA was concentrated with centrifugation at
          maximum velocity (>21000xg) at 4°C for 20 min. The aqueous solution
          was discarded and the visible (and invisible) RNA pellets were washed
          twice with 300 µL of ethanol solution 70% (v/v). The white gelatinous
          pellet (our invisible) was dried for 10 minutes at air room
          temperature and eluted with 20 µL of DEPC treated water preheated at
          65°C.
        </p>

        <b
          >Observation: Sometimes, the pellet will not be visible, but DO NOT
          WORRY! Continue the experiment and try to thoroughly elute the walls
          of the microtube with preheated DEPC water treated at 65°C.</b
