        <p>
          Plants 2 and 22 were the only intersection between biomolecular and
          biochemical experiments. Genomic PCR confirmed the presence of the
          construct in both lineages. Even though it was not possible to extract
          fresh pollen from plant number 2, pollen grains attached to its
          ovarium walls were stained during the flower staining experiment.
          Nevertheless, Let.it.bee's expression was later confirmed in plant
          number 2 through qRT-PCR.
        </p>

        <p>
          One might notice that the amount of stained pollen cells is fairly
          small. Additionally, the intensity of the blue hue also varies between
          grains. However odd this may look, those were expected results. The
          reason for that is that all experiments were carried out with T0
          plants (generation number 0 of the transgenic lineages). It is known
          that *Agrobacterium* mediated transformation happens randomly [5] and
          not all cells are transformed equally. Therefore, the plant presents a
          chimeric genotype in which the expression of the Let.it.bee construct
          is expected to vary among different cells and to be absent in the
          majority of them.
        </p>

        <p>
          The flower tissue staining procedure failed as the positive control we
          used for it did not stain. A possible reason for that is the lack of a
          vacuum pump to push x-gluc into the cells. Therefore, it wasn't
          possible to determine whether the LAT52 promoter was leaking to
          adjacent tissues. Nevertheless, that assumption was previously
          confirmed in the literature and later through qRT-PCR of leaf tissue.
        </p>

