          October 1st 2021 - Re-extraction of RNA from leafs plant 2 (Members:
          Caio K.)
        </h2>

        <p>
          We have extracted one more time the RNA from leafs of plant 2 using
          TRIzol™ Reagent (Invitrogen) and, finally, we could get RNA with high
          quality and integrity (Figure 15). The advice taken from this
          experiments is: do not worry if your RNA does not precipitates as a
          pellet and the microtube appearing to have nothing. The RNA may be
          stuck on the microtube walls after centrifugation so you can continue
          washing it with the ethanol solution until it gets unstuck. As another
          last resource, you may try to elute the invisible RNA with water
          (preheated at 65°C) on the edge of microtube. Remember that RNA is
          much smaller and we cannot see it sometimes!
        </p>

