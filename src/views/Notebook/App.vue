        <p>
          With the first and second question presented above being answered, we
          now needed to know the minimum amount of RNA mass we would need for
          cDNA synthesis. Once again, we used the RNA from the control plant and
          diluted it by 1000 ng, 500 ng, 200 ng, 100 ng and 50 ng for each
          solution of 10 µL of transcriptase reaction. We used the SuperScript
          II (Invitrogen), in accordance with their protocol. For each
          synthesis, we did a PCR amplification of Expressed sequence, a
          keephousing gene of tomato [2] donated to us by Prof. Dr. Maria
          Magdalena Rossi (GMP, IB-USP). The primers were designed to amplify a
          greater sequence when the template were genomic DNA and a smaller
          sequence when the template is from RNA (cDNA).
        </p>

        <p>
          The length difference in each amplicon was due to the presence of
          intron regions. It became a useful way of identifying if our RNA
          samples were contamined from genomic DNA or not. We used the Platinum™
          Taq DNA Polymerase (Invitrogen) protocol with 50 ng of DNA genomic as
          template and 2.5 µL from cDNA synthesis reaction. All reactions were
          made at 25 µL final volume. The conditions of the thermocycler were
          set up to 35 cycles, with primers anealling at 60°C and polymerase
          extension for 30 seconds. The agarose gel was made at 2%
          (weight/volume) concentration with Tris-Acetate-EDTA. The
          electrophoresis ran for 40 min at 90V.
        </p>

        <p>
          Our results show that it is possible to use masses as small as 50 ng
          of total RNA to synthesize a detectable cDNA fragment. Beyond this, we
          identified that our extraction and purification of RNA pollen was also
          good for qRT-PCR experiments because our samples didn't had any
          genomic DNA contamination after the degradation with DNAse (Figure 7).
