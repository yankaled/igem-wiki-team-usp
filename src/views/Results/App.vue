        <h2>THE COVID-19 PANDEMIC</h2>

        <p>
          The Covid-19 pandemic hit Brasil harshly throughout the past two
          years. Mismanagement of the country's public health services lead to a
          prolonged lockdown in our university, hindering access to the
          laboratories where we were to perform most of our wet-lab experiments.
          Therefore, most assays were performed in the final months of iGEM,
          when labs finally reopened. Despite the rush, we were able to obtain
          promising results regarding the integration and expression of
          Let.it.bee on our transgenic plants.
        </p>
        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/8/8a/T--USP-Brazil--IGEM-USP-Results-fullbinary.png/800px-T--USP-Brazil--IGEM-USP-Results-fullbinary.png"
          />
        </figure>

        <p>
          The principal construction (pLAT52-CYP6G1_LP4/2A_GUS) of ou project
          designed in silico is shown in Figure 1. The choice of architecture
          for the pLAT52 tomato promoter was based on findings reported by Bate
          and Twell (1998) [1]. The main characteristics of the promoter region
          described by the authors was the identification of all the cis
          elements of the promoter that confer its high specificity on pollen,
          and in addition its 5'UTR region was shown to contain a strong
          pollen-specific translational enhancer which increases translational
          yield ca. 13-fold [2]. Additionally, the codons of the CYP6G1 gene
          from <i>Drosophila melanogaster</i> (fruit fly) were modified in order
          to express the gene efficiently in tomato. In addition, the LP4/2A
          “self-cleaving” peptide sequence was inserted between the CYP6G1 and
          the GUS reporter gene sequence, respectively. This strategy allowed us
          to get the expression and translation of both enzymes in a single
          polypeptide chain, since eukaryotic systems do not allow the
          expression of more than one gene in a monocistronic mRNA. Thus, the
          fusion with the reporter gene made it possible to observe the specific
          location of the expression of CYP6G1 under the control of the pLAT52
          promoter.
        </p>

        <p>
          On the other hand, the binary vector backbone provided by the DNA
          Cloning Service company containing the repetitive DNA elements
          LefT-Border and Rigth-Border T-DNA flanking the ORF-1 and selection
          markers, allowed our construction to be inserted in the genome of
          tomato, and then the selection of transformant plants by the
          resistance to the antibiotic Kanamycin. Other elements present in the
          vector allowed its replication in the
          <i>Agrobacterium Tumefaciens</i> bacterium to transfer the plasmid to
          the plant, and also the replication in <i>E. coli</i> for for plasmid
          cloning and stock.
        </p>

        <p>
          The full vector was inserted into previously chemically competent
          <i>Agrobacterium Tumefaciens</i> GV3101 bacteria by heat-shock method
          described in the protocol
          <a href=" https://2021.igem.org/Team:USP-Brazil/Notebook"
            >(details on notebook).</a
          >
        </p>

        <p>
          The transfromation of Micro-tom GLK2 u/u was made by Pangeia Biotech
          with <i>A. tumefaciens</i> transfection methodology [3] (Figure 2).
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/6/68/T--USP-Brazil--IGEM-USP-Results-simple.png"
          />
        </figure>

        <p>
          The plasmid designed by us, pLAT52-CYP6G1_LP4/2A_GUS, was carried with
          success on for positive transformant resistents a kanamycin medium
          (Figure 3).
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/0/00/T--USP-Brazil--IGEM-USP-Results-florzinha.png/755px-T--USP-Brazil--IGEM-USP-Results-florzinha.png"
          />
        </figure>

        <p>
          We observed that the growth of T0 (first transformants) was slower
          than other control plants and have lower pollen production. After
          plants achieve 7 cm length, they were moved to a green house,
          localized in Departamento de Botânica - IB - USP (23°33'59.7"S
          46°43'49.9"W) (Figure 4). The green house was acclimatized for natural
          daylight and controlled temperature at 30°C. The transformed plants
          have grown sufficiently well and taken approximately one month to
          develop their reproductive system, necessary for pollen production.
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/e/e5/T--USP-Brazil--IGEM-USP-Results-IB.png/800px-T--USP-Brazil--IGEM-USP-Results-IB.png"
          />
        </figure>

        <p>
          After ten days, we went to green house and collected leafs for detect
          positive transformants for our circuit. One leaf, weigh about 20 mg,
          was sufficient for extracted the DNA, with good quality for detection
          of transcritps (more details on notebook). We planned to do a
          experiment of phenotyping amplifing the transcript that contein all
          sequence of our circuit (Figure 5), since pLAT52 until GUS, because we
          could have lose one of our parts during the plasmid integration
          process. The amplicon expected must have aproximatelly 2 kb lenght.
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/2/27/T--USP-Brazil--IGEM-USP-Results-presence.png/567px-T--USP-Brazil--IGEM-USP-Results-presence.png"
          />
        </figure>

        <p>
          We screened 12 random lines of transformant plants by PCR. After the
          validation of the quality of DNA extraction of the adapted methodology
          [4] (details on notebook), as the first PCR result, we found 3
          positives transformants to pLAT52-CYP6G1_LP4/2A_GUS presence: 2, 22
          and 25 (Figure 6).
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/8/83/T--USP-Brazil--IGEM-USP-Results-detection.png/800px-T--USP-Brazil--IGEM-USP-Results-detection.png"
          />
        </figure>

        <p>
          The PCR genomic indicated a successful integration of our cassette in
          the plant, but with lower quantity of tranformants compared with all,
          25% (3/12). It is can be explained by the random insertion after
          transfection by agrobacterium [5-6]. Due to the time it took for
          pollen production, we could only use the pollen from the transformant
          number 2 to quantify CYP6G1 and GUS transcripts. We used as control
          leafs for compare regulation expression by LAT52 promoter.
        </p>

        <p>
          Genic expression at transcript level was assessed for two target
          genes, CYP6G1 and GUS, and "Expressed", a reference gene for tomato.
          First, we obtained the high quality and integrity RNA for pollens and
          leafs for Micro-tom control and transformant number 2 and made a cDNA
          synthesis for mesagers all mensagers RNA using Oligod(T) as reverse
          primer. With samples, we quantify desired transcripts (Figure 7).
          RT-qPCR was made for detection of our circuit transcript and comparate
          with other plants that do not have it for proove our functionality
          construction.
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/d/dc/T--USP-Brazil--IGEM-USP-Results-steps.png/360px-T--USP-Brazil--IGEM-USP-Results-steps.png"
          />
        </figure>

        <p>
          RT-qPCR assays were successfully carried out and primers efficiencies
          were determined: 96.4% (r²=0.982) for CYP2, 95.8% (r²=0.979) for GUS,
          and 107.6% (r²=0.994) for "Expressed". Significative expression at
          transcript level for CYP2 (median Ct value of 21.4) and GUS (median Ct
          value of 20.5) genes were detected only for pollen of transgenic
          plants (median Ct values were above 31.9 for both genes in cDNA
          samples from pollen and leaves of control plants and from leaves of
          transgenic plants, as well as undetermined for both genes in negative
          control). These findings confirm the specific expression of CYP6G1 and
          GUS genes, at least at the transcriptional level, in the pollen of
          transgenic tomato plants and its functionality to produce in vivo
          transcripts (Table 1).
        </p>

        <p>
          The Ct represents the minimum threshold that the fluorescence must be
          detectable to indicate some amplification, without noises from machine
          detector. Pollen control and leaf from tranformant number 2 (T2)
          showed great Ct values and it was not expected for us. The
          contamination can be descarted, beacuse did not detected the
          significance Ct values for amplification of GUS transcripts and the
          pollen extraction of Micro-tom control and Micro-tom transformant 2
          was made in separate days. Look into it, the feasible answer for these
          results is the bad designed primers for CYP2 since it showed a
          significance value for reverse homo-dimer energy (-6.34 kcal/mol) when
          we evaluated with
          <a href="https://www.idtdna.com/calc/analyzer">
            OligoAnalyzer Tool by IDT</a
          >. But, how is it a hypothesis, we will need to do more experiments as
          biological replicate and repeating with other primers for CYP6G1 to
          explain better what have ocurred. Nevertheless, the proximity of Ct
          between GUS and CYP2 is a plausible value since they are products of
          the same transcript, regulated by promoter LAT52.
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/2/20/T--USP-Brazil--IGEM-USP-Results-Ct.png/800px-T--USP-Brazil--IGEM-USP-Results-Ct.png"
          />
        </figure>

        <p>
          The sequence pLAT52-CYP6G1_LP4/2A_GUS has the reporter gene from
          expression of β-glucuronidase (GUS) (translated together CYP6G1 on
          same transcript). Therefore, the activity of one of the two enzymes,
          if detected, would show strong evidence of the expression of the whole
          construct. GUS activity is fairly easy to detect as it metabolizes
          5-Bromo-4-Chloro-3-Indolyl β-D-Glucuronide (X-Gluc) into other c
          indigo blue (Figure 8). Nonetheless, it also allows histochemical
          colouring in pluricellular organisms, serving also as a tissue
          specificity indicator.
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/3/3b/T--USP-Brazil--IGEM-USP-Results-substrate.png/750px-T--USP-Brazil--IGEM-USP-Results-substrate.png"
          />
        </figure>

        <p>
          Positive results were obtained for transformants number 2 and 22,
          plants that we alredy knew were positive for our circuit. The negative
          and positive controls rendered correct results, validating the
          experiment (Figure 9).
        </p>

        <figure class="column has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/thumb/2/29/T--USP-Brazil--IGEM-USP-Results-GUS.png/504px-T--USP-Brazil--IGEM-USP-Results-GUS.png"
          />
        </figure>

        <p>
          Micro-tom 2 and 22 were confirmed to presence of GUS activiy. As we
          could not get pollen from plant 2 genomic PCR confirmed the presence
          of the construct in both lineages. Even though it was not possible to
          extract fresh pollen from plant number 2, pollen grains attached to
          its ovarium walls were stained during the flower staining experiment.
          Nevertheless, pLAT52-CYP6G1_LP4/2A_GUS expression was later confirmed
          in plant number 2 through RT-qPCR.
        </p>

        <p>
          One might notice that the amount of stained pollen cells is fairly
          small. Additionally, the intensity of the blue hue also varies between
          grains. However odd this may look, those were expected results. The
          reason for that is that all experiments were carried out with T0
          plants. It is known that agrobacterium mediated transformation happens
          randomly [5-6] and not all cells are transformed equally. Therefore,
          the plant presents a chimeric genotype in which the expression of the
          pLAT52-CYP6G1_LP4/2A_GUS construct is expected to vary among different
          cells and be absent in the majority of them.
        </p>

        <p>
          The flower tissue staining procedure failed as the positive control
          used for it did not stain. A possible reason for that is the lack of a
          vacuum pump to push X-Gluc into the cells. Therefore, it was not
          possible to determine whether the LAT52 promoter was leaking to
          adjacent tissues by GUS assay.
        </p>

        <p>
          Although problems of pandemic and worsening over 2020 year and middle
          of 2021 year, we tried to make the maximum contribution for iGEM. We
          always were studing how we could do the feasible experiments keeping
          careful for evitate to transmition of Sars-corona virus and after
          several discussions, we decided to do in silico experiments to
          optimize a CYP6G1 enzyme. At final months, with vaccine progress and
          death and contamination decrease in Brazil, some members (already
          vaccinated) made all efforts for proove functionallidade of our
          circuit and demonstrate some results of project Let.it.Bee. We were
          happy our preview results, and we were very happy too to show you the
          begin of Micro-tom that can help preserve bees in all world in a
          future near (Figure 10).
        </p>

