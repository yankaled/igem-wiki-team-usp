        <h2>More closely impacted by our project</h2>
        <h3>Goal 2: Zero Hunger</h3>
        <figure class="has-text-centered">
          <img
            style="width:10em;height:10em;"
            src="https://2021.igem.org/wiki/images/4/47/T--USP-Brazil--IGEM-USP-BestSustainableDevelopmentImpact-ZeroHunger.png"
          />
        </figure>
        <p>
          We strongly believe that our project addresses the purpose of this goal. Food security projections analysis conclude that 
          <b>
            food demands will increase
          </b> 
          by 35-56% till 2050, in a way that the 
          <b>
            risk of hunger
          </b> 
          will also change by -91% to +8% [1]. Other sources also cite the possibility of the percentage reaching 60% or even 110% [2,3]. 
          Possible overpopulation is pressuring the current food system, compelling producers to expand their crops by "cutting corners", 
          which includes 
          <b>
            depleting natural resources and even increasing the use of pesticides
          </b> 
          in a hope to quickly amplify their productions [4]. That can be a problem not only due to the countless environmental problems 
          associated with phytosanitary practices [4] but also because it will 
          <b>
            affect the quality of the food
          </b> 
          delivered to the people. The reason for the latter is that the misuse of pesticides causes the death of bees, insects that are 
          extremely important for the maintenance of life on earth [5]. 
        </p>
        <p>
          Bees are pollinators for many crops, and numerous plants are either dependent on their service or improved by it. About 70% of 
          crops species are reported to require their help [9]. With them around, productivity 
          <b>
            increases not only in quantity
          </b> 
          but also in quality. And 
          <b>
            without bees around to help in their reproduction some would cease to exist 
          </b>
          [6]. Among the benefited products there are fruits, oil crops, nuts, beans and much more [6]. With a decrease in bees populations, 
          we would have less food disponibility and the price would rise to the point of some of these items becoming luxuries.
        </p>
        <p>
          Therefore our project Let.it.bee. by helping 
          <b>
            keeping bees' populations safe 
          </b> 
          from pesticide toxicity and 
          <b> 
            maintaining the pesticide function
          </b> 
          against other pests, introduces a new way of achieving a more sustainable agriculture and food production.
        </p>    
        <p>
          With an expected increase and stable levels of pollination farmers can 
          <b>
            produce more and better food
          </b> 
          without having to resort to menacing solutions.
        </p>
        <h3>Goal 3: Good Health and well-being</h3>
        <figure class="has-text-centered">
          <img
            style="width:10em;height:10em;"
            src="https://2021.igem.org/wiki/images/thumb/2/28/T--Waterloo--Values1.png/120px-T--Waterloo--Values1.png"
          />
        </figure>
        <p>
          We believe that our health and well-being are 
          <b>
            intertwined with environmental preservation and education.
          </b>
          Based on this, one contribution related to our project is to improve the quality of life by saving bees. 
        </p>
        <p>
          Factors such as the preservation of the place where we live and knowledge about it are essential to avoid environmental and social 
          catastrophes. Not only this, but an indirect consequence of the project is also the increase in quality and diversity of food 
          worldwide, which makes a great positive impact on human health.
        </p>
        <p>
          The first thing we can think of the impact of bee pollination is in food quality. Usually,
          <b> 
            plants transfer more resources to their pollinated fruits,
          </b> 
          improving their taste and their quality. Fruits have shown that
          <b>
            their nutritional value is negatively impacted
          </b> 
          if they don't have diversity due to pollination. Lack of macro and micronutrients can harm human health in many ways, weakening the 
          immune system, negatively affecting development, increasing the mortality caused by other diseases, among other conditions [7, 8]. 
        </p>
        <p>
          On another note, pollination by bees also guarantees higher food production, some of the foods being totally dependent on their service. 
          Therefore, protecting bees assures the increase in food offer, 
          <b>
            aiding against the hunger crisis
          </b> 
          situation that is also a huge problem for humanity's health.
        </p>
        <p>
          Another important factor is that bees are also responsible for the pollination of plant species that are found in 
          <b>
            forests and woods,
          </b> 
          about 85% of them being dependent on their service [9]. Therefore, the preservation of bees means maintaining the diversity of plants of 
          all kinds, including the ones that are essential for 
          <b>
            pharmaceutical and medicinal purposes,
          </b>
          especially in certain communities [10]. Their substances are very important for treating conditions and diseases, as well as creating new 
          treatments. And also are a part of many cultural habits.
        </p>
        <h3>Goal 12: Responsible consumption and production</h3>
        <figure class="has-text-centered">
          <img
            style="width:10em;height:10em;"
            src="https://2021.igem.org/wiki/images/thumb/2/2c/T--USP-Brazil--IGEM-USP-BestSustainableDevelopmentImpact-Responsibleconsumption.png/600px-T--USP-Brazil--IGEM-USP-BestSustainableDevelopmentImpact-Responsibleconsumption.png"
          />
        </figure>
        <p>
          Responsible consumption and production are ways to ensure a sustainable future.
        </p>
        <p>
          As addressed in Goal 2: Zero Hunger it is expected that food demands will increase drastically in the near future, most probably causing a 
          growth in agriculture. So to ensure a sustainable food production growth it is essential to tackle the consequences of that growth and 
          <b>
            mitigate the impact over natural life.
          </b>
        </p>
        <p>
          One of the possible consequences strongly associated with agricultural growth is a greater use of pesticides, which can affect natural life 
          by the extension of cultivation area, maybe by getting near natural areas or worse by replacing native plants with pesticide-protected plants. 
          With our project we ensure that even if that happens the pollinators, key agents of natural preservation, 
          <b>
            won't be affected as much they would.
          </b>
        </p>
        <p>
          And as mentioned in Goal 3: Good Health and well-being pollination increases the production of more and better food. And by making food with 
          <b>
            better quality
          </b> 
          producers don't have the need to throw them away for having deformities, thus 
          <b>
            reducing food waste
          </b> 
          [14].
        </p>
        <h3>Goal 15: Life on land</h3>
        <figure class="has-text-centered">
          <img
            style="width:10em;height:10em;"
            src="https://2021.igem.org/wiki/images/thumb/3/32/T--USP-Brazil--IGEM-USP-BestSustainableDevelopmentImpact-Lifeonland.png/600px-T--USP-Brazil--IGEM-USP-BestSustainableDevelopmentImpact-Lifeonland.png"
          />
        </figure>
        <p>
          This goal is firmly related to our project, since 
          <b>
            bees are a part of life and essential for the maintenance of life!
          </b> 
          Preventing the decrease in their population by itself is already a positive impact for the maintenance of life on land.
        </p>
        <p>
          Ecosystemic relationships are strongly related, which makes communities vulnerable if interspecies dynamics is altered due to unbalanced species 
          population. The first point to be discussed is that with the absence of bees, many plants may cease to exist or diminish in number, due to the 
          lack of pollination or because of the level of dependence between some species [9]. That means the food chain will be disturbed, since many organisms 
          won't have access to plants nutrients, causing higher death rates on the long run, and affecting consequently other organisms that feed on those beings, 
          and so on and on. 
        </p>
        <p>
          The variety of diets in the world would also be harmed, compromising life through the emergence of possible diseases caused by 
          <b>
            widely spread nutrient deficiency
          </b> 
          and thus poor health conditions [7,8].
        </p>
        <p>
          Affecting the environment, it also affects the 
          <b>
            home of various forms of life.
          </b> 
          By decreasing pollination, plant life will be damaged, which cascades into other organisms (from micro to macro) that use these plants as homes and that 
          depends on the ecosystems for survival. The system is very sensitive and a 
          <b>
            domino effect
          </b> 
          is very possible. Loss of plant species can end up culminating into multiple extinction events [11].
        </p>
        <h3>Goal 8: Decent Work and Economic Growth</h3>
        <figure class="has-text-centered">
          <img
            style="width:10em;height:10em;"
            src="https://2021.igem.org/wiki/images/6/6a/T--USP-Brazil--IGEM-USP-BestSustainableDevelopmentImpact-DecentWork.png"
          />
        </figure>
        <p>
          For this goal, as already has been mentioned in the other sections, is evident that pollinators play an important role in food availability. Because of 
          their responsibility in crops productions they are accountable for about 
          <b>
            100 billion USD per year
          </b> 
          with their contribution to the agricultural GDP [19].
        </p>
        <p>
          Bees being out of the picture would mean a great decrease in profit, and the maintenance or increase of their populations would mean a boost in economy. 
          In developing countries, agriculture is usually the 
          <b>
            main source of income
          </b> 
          for 50% to 90% of the population, so stimulating rural activities by producing food in higher quantity and quality can strongly benefit the 
          <b>
            developing countries GDP
          </b> 
          and create 
          <b>
            more employment in rural communities
          </b> 
          [20]. 
        </p>
        <p>
          Therefore by saving bees we also grant economic growth for countries  by stimulating agriculture production, while avoiding a major loss in the world's 
          profit over their services.
        </p>

