        <p>
          The 17 Sustainable Development Goals (SDGs) of the United Nations aims to guarantee a 
          <b>
            sustainable and healthy future for the planet.
          </b> 
          They regard problems that societies are faced with and the global challenges that Earth is facing.
        </p>
