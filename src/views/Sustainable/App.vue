          <b>
            since its idealization.
          </b> 
          Bees have numerous impacts in the world because of their essential ecological services, which permeates multiple aspects 
          of human life and the environment. Through the development of our project and meetings with various 
          <b>
            stakeholders
          </b> 
          we dove deeper into how we are affected by bees and how much it could change with their disappearance.
        </p>
        <p>
          We have spoken to lots of 
          <b>
            research specialists
          </b> 
          from multiple areas, from bee ecotoxicology to pesticides application, companies, beekeepers and farmers. The one constante 
