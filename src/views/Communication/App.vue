        <h3>Implementation</h3>

        <p>
          After a year and a half of preparation, in June 2021, we launched the
          first-ever synbio olympiad in Brazil:
          <b>The Hacking Ecosystems Pocket Olympiad!</b> With
          <b>over 800 participants</b>, students from all over the country had
          their first contact with synbio and were hyped towards the official
          event. However, things weren’t as perfect as we had expected. Grades
          were low, and it seemed that almost about
          <b>3/8 of the students had abandoned the competition</b>. That
          prompted us to think that we might have gone too deep in synbio for
          the first-timers.
        </p>
        <figure class="has-text-centered">
          <img
            style="max-width:70% ; max-height:70%"
