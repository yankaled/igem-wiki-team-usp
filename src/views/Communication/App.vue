          frequent grades were 12,13 and 14, which shows that
          <b
            >most of them could absorb a good deal of information in this
            test.</b
          >
          Nonetheless, <b>61% declared themselves as females</b>, which shows an
          interesting inversion in gender balance when compared to other areas
          in STEM.
        </p>

