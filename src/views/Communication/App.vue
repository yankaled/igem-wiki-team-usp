        <h2>Brazilian Synthetic Biology Olympiad</h2>
        <figure class="has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/e/e1/T--USP-Brazil--IGEM-USP--Education-%26-Comunication-obbs.jpeg"
            alt=""
          />
