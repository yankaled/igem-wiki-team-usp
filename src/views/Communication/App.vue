        <small>Figure 1. Brazilian Synthetic Biology Olympiad's logo. </small>
        <p>
          During one of the Team’s earliest brainstorming sessions, we faced a
          complicated challenge. How to popularize synbio in Brazil? Many
          approaches were discussed until we found a common ground for the team
          members. Call it a coincidence or not, but we had all competed in
          several national and international science olympiads during High
          School, and we had fond memories of that time in our lives. Given that
          this kind of academic competition has grown extremely popular in our
          country for the past 20 years, we decided to profit from this familiar
          format for Brazilian students and create the first national synbio
          science olympiad.
        </p>
        <figure class="has-text-centered">
          <img
            src="https://2021.igem.org/wiki/images/5/54/T--USP-Brazil--IGEM-USP--Education-%26-Comunication-obbs-Member.jpeg"
            alt=""
          />
        </figure>
        <h3>Prospecting and Preparation</h3>

        <p>
          Everyone was excited about the project, but we had no idea how to make
          such a gigantic event possible. So, the first thing we did was to seek
          advice from former olympiad creators. We talked to the coordinators of
          <b>IYPT Brazil</b>, a practical physics competition, to understand
          whether there was any possibility to add experiments to our own.
          However, that seemed impractical due to the pandemic.
        </p>

        <p>
          Then, we interviewed the coordinator of the
          <b>Brazilian Physics Olympiad</b> (Olimpíada Brasileira de Física -
          OBF), who helped us understand a little more about the logistics of
          putting up a paper-based national test. Even Though the format of that
          competition resembled a little more what we wanted to do, there was
          still something missing. Physics is a mandatory subject in Brazilian
          High School, but synthetic biology is not. How could we make a test on
          a subject no one has ever studied?
        </p>

        <p>
          <b>João Canalle</b> was the one who helped us work that around.
          Professor Canalle did something very similar to what our Team aimed to
          do. He created the
          <b>Brazilian Astronomy and Astrophysics Olympiad</b>(OBA!) to
          popularize astronomy among Brazilian students. Just like synbio,
          astronomy is not taught in schools. Therefore, he had to come up with
          a way to overcome this barrier. His solution was to add as much
          information as he could in the texts of the questions, so the students
          could learn while taking the test! That was the solution we were
          looking for.
        </p>

        <p>
          Nevertheless, there was still a major challenge to surpass. All those
          olympiads were paper-based and demanded a logistic effort the team
          could not afford to put into the competition. We mentioned that to
          professor Canalle, who then gave us the contact of a friend that he
          thought had all the answers to our problems.
        </p>

        <p>
          That’s when we met <b>Daniel</b>. He, who’s also a professor, had
          created several different olympiads in the same informative style as
          professor Canalle. However, his tests had a little something extra:
          they were virtual. That not only solved the logistic issue but also
          opened up a world of possibilities regarding media format. That meant
          we could add authentic synbio material to our tests, including videos
          from iGEM TV and other teams’ wikis. **A year later Daniel would be
          invited to work for the Brazilian Ministry of Science and Technology
          and offer federal government support for our Team's olympiad.
        </p>

        <p>
          After that interview we had our plan: To make a virtual, informative,
          national olympiad in Brazil called
          <b>OBBS - Brazilian Synthetic Biology Olympiad!</b>
        </p>

        <p>
          Shortly after we contacted <b>Fractal</b>, a company specialized in
          digital learning that had created the platforms for Daniel’s
          olympiads. They were receptive to our idea and offered to partner with
          us through their non-profit branch <b>Instituto Vertere</b>.
          Nonetheless, they suggested we make a Pocket Olympiad, a short and
          thematic test to try out our model before moving to the grand national
          competition.
        </p>
        <figure class="has-text-centered">
          <img
            style="max-width:80% ; max-height:80%"
