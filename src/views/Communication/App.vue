        <p>
          That was a lesson we took seriously when preparing for the actual
          event. Brazil is a deeply unequal country when it comes to education.
          So, if we were to popularize synbio here, we had to level the students
          first.
        </p>
        <figure class="has-text-centered">
          <img
            style="max-width:70% ; max-height:70%"
            src="https://2021.igem.org/wiki/images/e/ef/T--USP-Brazil--IGEM-USP--Education-%26-Comunication-Workflow.jpeg"
            alt=""
          />
