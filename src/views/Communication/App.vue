        <h3>The Scientific Camp's mission</h3>
        <p>
          To expand students' scientific wonder and knowledge through logical
          activities in public schools of the state of São Paulo, using
          children's daily lives as a basis for interactive and fun activities
          going beyond expository classes in a conventional classroom; to awaken
          their interest in science, bringing children closer to the scientific
          work in a pleasurable and stimulating way; and to help the development
          of scientific skills, such as taking a critical look at the world,
          discussing and creating hypothesis to solve problems, in addition to
          encouraging teamwork, with monitors and with colleagues.
