        <h4>Microbe Drop</h4>
        <p>
          The <i>Microbe Drop</i> is an experiment where a laser pen pointing at
          a contaminated drop of water is used. The beam passes through the
          drop, and you can see the shadows of the microorganisms living in the
          water on the wall, like a projector. The activity is designed to
          emphasize the importance of filtered water;
        </p>
        <h4>Petri Dish</h4>
        <p>
          The <i>Petri Dish</i> was the only in person activity, respecting all
          the sanitary criteria required by the context of the COVID-19
          pandemic. Petri dishes with complex medium were prepared, where each
          child left a handprint: one before washing and one after washing. The
          intention was, after two weeks, to compare the plates and show the
          microorganisms that grew there, emphasizing the importance of hand
          washing.
        </p>
        <h4>Coarse Salt Crystallization</h4>
        <p>
          The <i>Coarse Salt Crystallization</i> is an activity that seeks to
          understand supersaturated solutions. The solution, supersaturated with
          coarse salt, crystallizes at the bottom of a container after heating
          the water, forming crystal
        </p>
        <h4>CO2 Balloon</h4>
        <p>
          The <i>CO2 Balloon</i> activity uses the chemical reaction between
          vinegar (acetic acid)  and sodium bicarbonate inside a bottle with a
          balloon in the spout to produce CO2, and thus quickly fill the party
          bladder with the gas from reaction.
        </p>
        <h4>CinePalito</h4>
        <p>
          At <i>CinePalito</i>, the pedagogical theme was optical illusions. The
          children were given two pieces of paper, one with a picture of a cage
          and the other with blank space, and asked to draw a monster on the
          latter. Then the papers were glued with the drawings out, with a
          barbecue stick in the middle so that they could be rotated. By quickly
          rotating the pasted papers an optical illusion is created that joins
          the two images into one, creating the illusion of trapping the
          monster.
        </p>
        <h4>Ecological Bingo</h4>
        <p>
          <i>Ecological Bingo</i> was an activity created in partnership with
          "Estação Biologia" that uses a card with five ecosystems and five
          characteristics of each one: a general photo, an example of fauna, an
          example of flora, an abiotic characteristic and a problem
          environmental. The rows of photos are shuffled, and the children's
          goal is to relate the biomes to their respective characteristics,
          painting each photo with a corresponding color, similar to a bingo
          game.
        </p>
        <h4>Food Chain</h4>
        <p>
          In the <i>Food Chain</i> activity, children receive a personalized
          picture with various members of an ecosystem's food web: producers,
          primary, secondary and tertiary consumers. The children's goal is to
          trace as many food chains as possible, thus forming a complex food web
          with diverse trophic relationships. Note: this activity unfortunately
          could not be applied in the Scientific Camp of 2021.
        </p>
        <h4>Memory Game</h4>
        <p>
          The <i>Memory Game</i> was held on the Kahoot platform, with the
          intention of reminding children on the last day of the Camp of
          important moments in history and the contents worked during the weeks
          of application. The questions included concepts worked on in all
          previous activities: the Microbe Drop, Petri Dish, Coarse Salt
          Crystallization, CO2 Balloon, CinePalito and Ecological Bingo. related
          to pollination to be discovered. Each of them has a different style,
          with two figures representing bees and pollinators, respectively, one
          about the diversity of animal pollinators, a concrete challenge
          related to flowers and finally, a crossword with 10 words in
          Portuguese.
        </p>
