        <p>
          In the light of the social and economic scenario Brazil faces, we
          tried to <b>shape the olympiad to be as accessible as possible</b> for
          people from as many backgrounds as possible. We started by making the
          first phase non-eliminatory, so even if a student did badly on it he
          or she would not be barred from the second one. Nonetheless, students
          had a week to complete each phase. That means that low-income kids
          would have time to borrow a parent's or a friend's phone, or use a
          public library to take the test, in case they didn't have a device of
          their own. Considering that each phase introduces the participants to
          increasingly complex concepts in synbio, it wouldn't be fair to keep
          the students from accessing half this info solely because of a bad
          grade.
        </p>

        <p>
          The second action was to <b>assume they had no basis in biology</b>.
          Therefore, even simple concepts, like cells and DNA were explained
          either in the question's text, in a glossary or through multimedia
          (videos, gifs, and pictures). Finally, we added
          <b>affirmative actions</b> on the olympiad's statute to guarantee that
          underrepresented communities, such as students from public schools,
          black and indigenous pupils would have their share on both the medals
          and in the special prize offered.
        </p>

        <p>
          Speaking of the special prize, as an ultimate award, the top 10
          students would be invited to a
          <b
            >scientific immersion in one of Latin America's most prestigious
            universities, The University of São Paulo.</b
          >
          In this one-week trip to a vibrant research and entrepreneurship
          environment in the city of São Paulo, we hope to inspire students to
          further their interests in synbio and, ultimately, pursue a career in
          one of its correlated areas. To secure the means to provide this
          award, we partnered with our
          <b>University's Dean of Culture and Extension</b> who gave us
          permission to use USP's logo, facilities and financial aid.
        </p>

