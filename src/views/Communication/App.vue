        <h4>How?</h4>
        <p>
          We were inspired by a method called <i><b>gamification</b></i
          >, which consists of
          <b>using game mechanics to improve children's learning</b>. It is
          widely used nowadays, mainly for educational purposes. As more and
          more games are part of the daily lives of young people, this strategy
          establishes the learning process in an interactive way. Then, we
          studied through books and internet media about this methodology of
          teaching. However, we were not fulfilled by reading only, making us
          put this knowledge into practice in our scientific camp.
