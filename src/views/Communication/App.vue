        <p>
          In the meantime, we contacted two professors from our university’s
          <b>Institute of Biology</b>, <b>Prof. Dra. Cris Arias</b> and
          <b>Prof. Dra. Maria Elice</b>, to help us understand the base for
          teaching biological concepts by investigation.
        </p>

        <p>
          This method’s principle comes from the idea of presenting the student
          a new concept together with a problem and asking them the correct
          hypothesis. The first questions were used as a “warm-up” and to
          introduce basic concepts, like cell, enzyme, protein, DNA, RNA and the
          central dogma of molecular biology for example; but in the following
          questions new concepts, using the basic ones as a foundation,  were
          presented, together with some logic and interpretative problems.
          Another powerful teaching tool we used was the creation of a
          “glossary” containing short descriptions of these new concepts and
          ideas, making it easier for students to find and consult these ideas.
        </p>

